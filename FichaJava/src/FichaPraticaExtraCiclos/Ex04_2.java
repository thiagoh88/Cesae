package FichaPraticaExtraCiclos;

public class Ex04_2 {
    public static void main(String[] args) {
        int altura = 5;

        for (int i = 0; i < altura; i++)
        {
            System.out.println();
            for (int j = 0; j < 1 * i + 1; j++)
            {
                System.out.print("*");
            }
        }
            System.out.println();
        }
    }
